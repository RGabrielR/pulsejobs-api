import { ApiProperty } from '@nestjs/swagger';
import { JobResultStatus } from '@prisma/client';

export class JobResultItemResponseDto {
  @ApiProperty()
  id!: string;

  @ApiProperty()
  jobId!: string;

  @ApiProperty()
  rowNumber!: number;

  @ApiProperty({ enum: JobResultStatus })
  status!: JobResultStatus;

  @ApiProperty()
  message!: string;

  @ApiProperty({ type: 'object', additionalProperties: true })
  rawData!: Record<string, unknown>;

  @ApiProperty({ type: 'object', additionalProperties: true, nullable: true })
  normalizedData!: Record<string, unknown> | null;

  @ApiProperty({
    type: 'array',
    items: { type: 'string' },
    nullable: true,
    description: 'Row-level warnings generated by flexible ingestion/validation.',
  })
  warnings!: string[] | null;

  @ApiProperty()
  createdAt!: Date;
}
